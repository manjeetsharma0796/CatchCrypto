const contractAddress = '0x63846e146420ff19C6b870878A189A922f2b2739';
const accountAddress = '0xfB8ae9808D84BF601f2Ef6178Da51a612bD046D0';


// AIA Testnet RPC URL
const provider = new ethers.providers.JsonRpcProvider('https://aia-dataseed1-testnet.aiachain.org');

// ERC-20 Token Contract ABI (you may need to replace this if your token has a custom ABI)
const tokenAbi = Token.abi;

// Replace with your ERC-20 token contract address
const tokenAddress = contractAddress; // Replace with actual token contract address

// Replace with the address you want to check the token balance of
const walletAddress = accountAddress; // Replace with actual wallet address

async function getTokenBalance() {
    try {
        // Create a contract instance for the ERC-20 token
        const tokenContract = new ethers.Contract(tokenAddress, tokenAbi, provider);

        // Get the balance in raw token units
        const balance = await tokenContract.balanceOf(walletAddress);

        // Get token decimals to format the balance
        const decimals = await tokenContract.decimals();

        // Format the balance using decimals
        const formattedBalance = ethers.utils.formatUnits(balance, decimals);

        console.log(`Token Balance: ${formattedBalance} tokens`);
    } catch (error) {
        console.error('Error fetching token balance:', error);
    }
}

getTokenBalance();
